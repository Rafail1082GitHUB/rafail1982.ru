                await loadPlaylist();
                loadSavedSettings();
                setupEventListeners();
            }

            // –ó–∞–≥—Ä—É–∑–∫–∞ –ø–ª–µ–π–ª–∏—Å—Ç–∞ –∏–∑ M3U —Ñ–∞–π–ª–∞ —Å –∫—ç—à–∏—Ä–æ–≤–∞–Ω–∏–µ–º
            async function loadPlaylist() {
                try {
                    const response = await fetch('../radio/radio.m3u', {
                        cache: 'force-cache',
                        headers: {
                            'Cache-Control': 'max-age=300' // –ö—ç—à –Ω–∞ 5 –º–∏–Ω—É—Ç
                        }
                    });
                    if (!response.ok) {
                        throw new Error(`–û—à–∏–±–∫–∞ HTTP: ${response.status}`);
                    }
                    
                    const m3uContent = await response.text();
                    parseM3UPlaylist(m3uContent);
                    
                } catch (error) {
                    console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –ø–ª–µ–π–ª–∏—Å—Ç–∞:', error);
                    loadFallbackStations();
                }
            }

            // –ü–∞—Ä—Å–∏–Ω–≥ M3U –ø–ª–µ–π–ª–∏—Å—Ç–∞ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function parseM3UPlaylist(m3uContent) {
                radioStationsData = [];
                const lines = m3uContent.split('\n');
                let currentStation = {};

                for (let i = 0; i < lines.length; i++) {
                    const line = lines[i].trim();
                    
                    if (line.startsWith('#EXTINF:')) {
                        const match = line.match(/#EXTINF:.*?,(.*)/);
                        if (match && match[1]) {
                            currentStation.text = match[1].trim();
                        }
                    } else if (line.startsWith('http')) {
                        currentStation.value = line;
                        if (currentStation.text && currentStation.value) {
                            radioStationsData.push({...currentStation});
                            currentStation = {};
                        }
                    }
                }

                populateStationsList();
            }

            // –†–µ–∑–µ—Ä–≤–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å—Ç–∞–Ω—Ü–∏–π (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function loadFallbackStations() {
                radioStationsData = [
                    { "value": "http://tabassum.uz:9000/alo_low", "text": "A'lo FM" },
                    { "value": "http://truckfm.rafail1982.uz:8000/64", "text": "TRUCK FM" },
                    { "value": "https://live.hitfm.uz/hitfmuz", "text": "Hit.FM" },
                    { "value": "https://stream-175.zeno.fm/y8gdyaatxhhvv", "text": "MARS FM" },
                    { "value": "https://stream-165.zeno.fm/64ox9cg1w0quv", "text": "TECHNORADIO" },
                    { "value": "https://tempradio-live.uz/streamaac", "text": "TEMP_FM" },
                    { "value": "http://dono.rafail1982.uz:8000/fm", "text": "Oriat FM" },
                    { "value": "http://live.qalbimnavosi.uz/qalbimnavosi", "text": "QALBIM NAVOSI" },
                    { "value": "http://mediamarkaz.com:8000/radiomarkaz", "text": "MARKAZ FM" },
                    { "value": "http://dono.rafail1982.uz:8000/dono", "text": "ORIAT DONO" }
                ];
                populateStationsList();
            }

            // –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ —Å—Ç–∞–Ω—Ü–∏–π (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function populateStationsList() {
                radioStations.innerHTML = '';
                
                if (radioStationsData.length === 0) {
                    const option = document.createElement('option');
                    option.value = '';
                    option.textContent = '–ù–µ—Ç –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Å—Ç–∞–Ω—Ü–∏–π';
                    radioStations.appendChild(option);
                    return;
                }

                radioStationsData.forEach((station, index) => {
                    const option = document.createElement('option');
                    option.value = station.value;
                    option.textContent = station.text;
                    option.dataset.index = index;
                    radioStations.appendChild(option);
                });

                stationInfo.textContent = '–í—ã–±–µ—Ä–∏—Ç–µ —Å—Ç–∞–Ω—Ü–∏—é';
            }

            // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞—É–¥–∏–æ —ç–ª–µ–º–µ–Ω—Ç–∞ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function setupAudioElement() {
                audio.volume = 0.5;
                audio.preload = 'none';
                
                audio.addEventListener('playing', () => {
                    isPlaying = true;
                    updatePlayButton();
                    stationInfo.textContent = '‚ñ∂ ' + radioStations.options[radioStations.selectedIndex].text;
                });

                audio.addEventListener('pause', () => {
                    isPlaying = false;
                    updatePlayButton();
                    stationInfo.textContent = '‚ùö‚ùö ' + radioStations.options[radioStations.selectedIndex].text;
                });

                audio.addEventListener('error', (e) => {
                    console.error('–û—à–∏–±–∫–∞ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è:', e);
                    stationInfo.textContent = '–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ —Å—Ç–∞–Ω—Ü–∏–∏';
                    isPlaying = false;
                    updatePlayButton();
                });

                audio.addEventListener('waiting', () => {
                    stationInfo.textContent = '–ë—É—Ñ–µ—Ä–∏–∑–∞—Ü–∏—è...';
                });
            }

            // –ó–∞–≥—Ä—É–∑–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã—Ö –Ω–∞—Å—Ç—Ä–æ–µ–∫ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function loadSavedSettings() {
                const savedStationIndex = localStorage.getItem('selectedStation');
                const savedVolume = localStorage.getItem('volume');
                
                if (savedStationIndex !== null && radioStationsData.length > 0) {
                    const index = parseInt(savedStationIndex);
                    if (index >= 0 && index < radioStationsData.length) {
                        radioStations.selectedIndex = index;
                        currentStationIndex = index;
                    }
                }
                
                if (savedVolume !== null) {
                    volumeControl.value = savedVolume * 100;
                    audio.volume = parseFloat(savedVolume);
                }
            }

            // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ —Å–æ–±—ã—Ç–∏–π (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function setupEventListeners() {
                audio.addEventListener('ended', () => {
                    if (isPlaying) {
                        setTimeout(() => playStation(), 2000);
                    }
                });
            }

            // –í–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —Å—Ç–∞–Ω—Ü–∏–∏ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function playStation() {
                if (radioStationsData.length === 0) {
                    stationInfo.textContent = '–ù–µ—Ç –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Å—Ç–∞–Ω—Ü–∏–π';
                    return;
                }

                const selectedOption = radioStations.options[radioStations.selectedIndex];
                const streamUrl = selectedOption.value;
                
                if (!streamUrl) {
                    stationInfo.textContent = '–ù–µ–≤–µ—Ä–Ω—ã–π URL —Å—Ç–∞–Ω—Ü–∏–∏';
                    return;
                }

                audio.pause();
                audio.src = streamUrl;
                
                audio.play().catch(error => {
                    console.error('–û—à–∏–±–∫–∞ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è:', error);
                    stationInfo.textContent = '–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Å—Ç–∞–Ω—Ü–∏–∏';
                });

                currentStationIndex = radioStations.selectedIndex;
                localStorage.setItem('selectedStation', currentStationIndex);
                
                stationInfo.textContent = '–ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ...';
            }

            // –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –≥—Ä–æ–º–∫–æ—Å—Ç—å—é (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function changeVolume(amount) {
                let newVolume = parseFloat(volumeControl.value) + amount;
                newVolume = Math.max(0, Math.min(100, newVolume));
                volumeControl.value = newVolume;
                setVolume();
            }

            function setVolume() {
                const newVolume = parseFloat(volumeControl.value) / 100;
                audio.volume = newVolume;
                localStorage.setItem('volume', newVolume);
            }

            // –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ–º (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function togglePlayPause() {
                if (radioStationsData.length === 0) {
                    stationInfo.textContent = '–ù–µ—Ç –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Å—Ç–∞–Ω—Ü–∏–π';
                    return;
                }

                if (isPlaying) {
                    audio.pause();
                } else {
                    if (audio.src) {
                        audio.play();
                    } else {
                        playStation();
                    }
                }
            }

            function updatePlayButton() {
                playButton.textContent = isPlaying ? '‚ùö‚ùö' : '‚ñ∂';
            }

            // –°–º–µ–Ω–∞ —Å—Ç–∞–Ω—Ü–∏–∏ —á–µ—Ä–µ–∑ select (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function changeStationFromList() {
                playStation();
            }

            // –ù–∞–∑–Ω–∞—á–µ–Ω–∏–µ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ —Å–æ–±—ã—Ç–∏–π (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            playButton.addEventListener('click', togglePlayPause);
            radioStations.addEventListener('change', changeStationFromList);
            volumeControl.addEventListener('input', setVolume);
            volumeDownBtn.addEventListener('click', () => changeVolume(-1));
            volumeUpBtn.addEventListener('click', () => changeVolume(1));

            // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è
            initPlayer();
        }

        // –ö–æ–¥ –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤ (–æ–ø—Ç–∏–º–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω—ã–π)
        function initPlaylistUpdater() {
            const API_URL = 'save_playlist.php';
            const DATA_URL = 'playlist_data.php';
            
            let playlists = [];
            const statusMessage = document.getElementById('statusMessage');
            const updateAllBtn = document.getElementById('updateAllBtn');
            
            // –ü–æ–∫–∞–∑–∞—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ –æ —Å—Ç–∞—Ç—É—Å–µ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            function showStatus(message, type = 'success') {
                statusMessage.textContent = message;
                statusMessage.style.display = 'block';
                statusMessage.style.backgroundColor = type === 'success' ? '#d4edda' : 
                                                    type === 'error' ? '#f8d7da' : '#fff3cd';
                statusMessage.style.color = type === 'success' ? '#155724' : 
                                           type === 'error' ? '#721c24' : '#856404';
                statusMessage.style.border = type === 'success' ? '1px solid #c3e6cb' : 
                                            type === 'error' ? '1px solid #f5c6cb' : '1px solid #ffeaa7';
                
                setTimeout(() => {
                    statusMessage.style.display = 'none';
                }, 5000);
            }
            
            // –ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤ —Å —Å–µ—Ä–≤–µ—Ä–∞ —Å –∫—ç—à–∏—Ä–æ–≤–∞–Ω–∏–µ–º
            async function loadPlaylistsFromServer() {
                try {
                    const response = await fetch(DATA_URL + '?action=load&t=' + Date.now(), {
                        cache: 'no-cache'
                    });
                    const result = await response.json();
                    
                    if (!response.ok) {
                        throw new Error(result.error || 'Server error');
                    }
                    
                    return result.playlists || [];
                } catch (error) {
                    console.error('Error loading playlists:', error);
                    return [];
                }
            }
            
            // –û—Ç–ø—Ä–∞–≤–∏—Ç—å –∑–∞–ø—Ä–æ—Å –Ω–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –ø–ª–µ–π–ª–∏—Å—Ç–∞ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            async function savePlaylistToServer(playlist) {
                try {
                    const response = await fetch(API_URL, {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                        },
                        body: JSON.stringify({
                            url: playlist.url,
                            filename: playlist.fileName
                        })
                    });
                    
                    const result = await response.json();
                    
                    if (!response.ok) {
                        throw new Error(result.error || 'Server error');
                    }
                    
                    return result;
                } catch (error) {
                    console.error('Error saving playlist:', error);
                    throw error;
                }
            }
            
            // –û–±–Ω–æ–≤–∏—Ç—å –ø–ª–µ–π–ª–∏—Å—Ç (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            async function updatePlaylist(index) {
                const playlist = playlists[index];
                
                try {
                    showStatus(`–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–ª–µ–π–ª–∏—Å—Ç–∞ "${playlist.name}"...`, 'info');
                    
                    const result = await savePlaylistToServer(playlist);
                    
                    const now = new Date();
                    playlists[index].lastUpdate = now.toISOString();
                    playlists[index].lastSize = result.file_size;
                    
                    showStatus(`–ü–ª–µ–π–ª–∏—Å—Ç "${playlist.name}" —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω! –†–∞–∑–º–µ—Ä: ${formatFileSize(result.file_size)}`, 'success');
                    
                } catch (error) {
                    console.error('Error updating playlist:', error);
                    showStatus(`–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ "${playlist.name}": ${error.message}`, 'error');
                }
            }
            
            // –û–±–Ω–æ–≤–∏—Ç—å –≤—Å–µ –ø–ª–µ–π–ª–∏—Å—Ç—ã (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            async function updateAllPlaylists() {
                if (playlists.length === 0) {
                    showStatus('–ù–µ—Ç –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤ –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è', 'warning');
                    return;
                }
                
                updateAllBtn.innerHTML = '–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ...';
                updateAllBtn.classList.remove('btn-pulse');
                
                try {
                    showStatus('–ù–∞—á–∏–Ω–∞–µ—Ç—Å—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤—Å–µ—Ö –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤...', 'info');
                    
                    for (let i = 0; i < playlists.length; i++) {
                        await updatePlaylist(i);
                        await new Promise(resolve => setTimeout(resolve, 1000));
                    }
                    
                    showStatus('–í—Å–µ –ø–ª–µ–π–ª–∏—Å—Ç—ã —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω—ã!', 'success');
                    
                } catch (error) {
                    console.error('Error updating all playlists:', error);
                    showStatus(`–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤: ${error.message}`, 'error');
                } finally {
                    setTimeout(() => {
                        updateAllBtn.innerHTML = '–û–±–Ω–æ–≤–∏—Ç—å –≤—Å–µ –ø–ª–µ–π–ª–∏—Å—Ç—ã';
                        updateAllBtn.classList.add('btn-pulse');
                    }, 2000);
                }
            }
            
            function formatFileSize(bytes) {
                if (!bytes) return '';
                if (bytes < 1024) return bytes + ' B';
                if (bytes < 1048576) return (bytes / 1024).toFixed(2) + ' KB';
                return (bytes / 1048576).toFixed(2) + ' MB';
            }
            
            // –ó–∞–≥—Ä—É–∑–∏—Ç—å –¥–∞–Ω–Ω—ã–µ —Å —Å–µ—Ä–≤–µ—Ä–∞ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            async function loadFromServer() {
                playlists = await loadPlaylistsFromServer();
                
                if (playlists.length > 0) {
                    console.log(`–ó–∞–≥—Ä—É–∂–µ–Ω–æ ${playlists.length} –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤`);
                    showStatus(`–ó–∞–≥—Ä—É–∂–µ–Ω–æ ${playlists.length} –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤ –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è`, 'success');
                }
            }
            
            // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –¥–ª—è –∫–Ω–æ–ø–∫–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –≤—Å–µ—Ö –ø–ª–µ–π–ª–∏—Å—Ç–æ–≤ (–±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π)
            updateAllBtn.addEventListener('click', updateAllPlaylists);
            
            // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –¥–∞–Ω–Ω—ã—Ö
            loadFromServer();
        }
